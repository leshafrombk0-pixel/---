#Даны целые положительные числа A и B (A > B). На отрезке длины
#A размещено максимально возможное количество отрезков длины B (без наложений).
#Используя операцию взятия остатка от деления нацело, найти длину незанятой части
#отрезка A.

from functools import reduce

# --- Входные данные ---
get_int = lambda prompt: int(input(prompt))

A = get_int("Введите A (длина большого отрезка): ")
B = get_int("Введите B (длина малого отрезка, B < A): ")

# --- Чистые функции ---
count_segments  = lambda a, b: a // b       # кол-во отрезков B, которые влезают
unused_length   = lambda a, b: a % b        # незанятая часть

# --- Результат ---
print(f"\nКоличество отрезков B: {count_segments(A, B)}")
print(f"Незанятая часть отрезка A: {unused_length(A, B)}")
