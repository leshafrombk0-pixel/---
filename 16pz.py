# ==============================================================================
# Наследование классов (Животное -> Собака)
# ==============================================================================


class Animal:
    """Базовый родительский класс 'Животное'."""

    def __init__(self, species: str, paws_count: int, fur_color: str):
        """Конструктор для инициализации общих свойств животного."""
        self.species = species  # Свойство "вид"
        self.paws_count = paws_count  # Свойство "количество лап"
        self.fur_color = fur_color  # Свойство "цвет шерсти"

    def show_animal_info(self):
        """Метод для вывода базовой информации о животном."""
        print(
            f"Вид: {self.species}, Лап: {self.paws_count}, Цвет шерсти: {self.fur_color}"
        )


class Dog(Animal):
    """Дочерний класс 'Собака', наследующий свойства класса 'Животное'."""

    def __init__(
        self,
        species: str,
        paws_count: int,
        fur_color: str,
        name: str,
        breed: str,
    ):
        """Конструктор собаки, расширяющий свойства родительского класса."""
        # super() автоматически передает общие свойства в класс Animal
        super().__init__(species, paws_count, fur_color)
        self.name = name  # Дополнительное свойство "кличка"
        self.breed = breed  # Дополнительное свойство "порода"

    def show_dog_info(self):
        """Метод для вывода полной информации о собаке."""
        print(
            f"Кличка: {self.name}, Порода: {self.breed}, "
            f"Вид: {self.species}, Лап: {self.paws_count}, Цвет шерсти: {self.fur_color}"
        )


