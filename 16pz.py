# БЛОК 1: Базовый класс "Здание"
# ==============================================================================


class Building:
    """Класс, описывающий здание."""

    def __init__(self, address: str, floors_count: int):
        """Инициализация атрибутов здания."""
        self.address = address  # Атрибут "адрес"
        self.floors_count = floors_count  # Атрибут "количество этажей"

    def display_info(self):
        """Метод для вывода информации о здании в заданном формате."""
        print(f"Адрес: {self.address}, Количество этажей: {self.floors_count}")


# ==============================================================================
# БЛОК 2: Наследование классов (Животное -> Собака)
# ==============================================================================


class Animal:
    """Базовый класс 'Животное'."""

    def __init__(self, species: str, paws_count: int, fur_color: str):
        """Инициализация общих свойств животного."""
        self.species = species  # Свойство "вид"
        self.paws_count = paws_count  # Свойство "количество лап"
        self.fur_color = fur_color  # Свойство "цвет шерсти"

    def show_animal_info(self):
        """Метод для вывода информации о животном."""
        print(
            f"Вид: {self.species}, Лап: {self.paws_count}, Цвет шерсти: {self.fur_color}"
        )


class Dog(Animal):
    """Дочерний класс 'Собака', наследуемый от класса 'Животное'."""

    def __init__(
        self,
        species: str,
        paws_count: int,
        fur_color: str,
        name: str,
        breed: str,
    ):
        """Инициализация свойств собаки с учетом родительского класса."""
        # Вызываем конструктор базового класса Animal для заполнения общих свойств
        super().__init__(species, paws_count, fur_color)
        self.name = name  # Новое свойство "кличка"
        self.breed = breed  # Новое свойство "порода"

    def show_dog_info(self):
        """Метод для вывода полной информации о собаке."""
        print(
            f"Кличка: {self.name}, Порода: {self.breed}, "
            f"Вид: {self.species}, Лап: {self.paws_count}, Цвет шерсти: {self.fur_color}"
        )


